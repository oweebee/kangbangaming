import { useState, useEffect, useCallback } from 'react';

// ── Langues supportées ──────────────────────────────────────────────────────
export const SUPPORTED_LANGS = ['fr', 'en', 'es', 'de', 'ru', 'zh'];

export const LANG_META = {
  fr: { name: 'Français',  flag: '🇫🇷' },
  en: { name: 'English',   flag: '🇬🇧' },
  es: { name: 'Español',   flag: '🇪🇸' },
  de: { name: 'Deutsch',   flag: '🇩🇪' },
  ru: { name: 'Русский',   flag: '🇷🇺' },
  zh: { name: '中文',       flag: '🇨🇳' },
};

// ── État global (module-level) ────────────────────────────────────────────
let _lang = 'fr';
let _userId = null;
const _subscribers = new Set();

function _detectBrowserLang() {
  const nav = (navigator?.language || '').split('-')[0].toLowerCase();
  return SUPPORTED_LANGS.includes(nav) ? nav : 'en';
}

/** Appelé au login / au chargement de l'auth sauvegardée */
export function initUserLang(userId) {
  _userId = userId || null;
  if (!userId) {
    _lang = _detectBrowserLang();
  } else {
    try {
      const stored = localStorage.getItem(`lang_${userId}`);
      _lang = (stored && SUPPORTED_LANGS.includes(stored)) ? stored : _detectBrowserLang();
    } catch {
      _lang = _detectBrowserLang();
    }
  }
  _subscribers.forEach(fn => fn(_lang));
}

/** Changer la langue (stockée par userId) */
export function setLang(lang) {
  if (!SUPPORTED_LANGS.includes(lang)) return;
  _lang = lang;
  try { localStorage.setItem(`lang_${_userId || 'guest'}`, lang); } catch {}
  _subscribers.forEach(fn => fn(lang));
}

/** Lecture directe de la langue active — pour usage hors composants React (ex. utils.js) */
export function getLang() {
  return _lang;
}

/** Hook React — s'abonne aux changements de langue */
export function useLang() {
  const [lang, setLangState] = useState(_lang);
  useEffect(() => {
    const handler = l => setLangState(l);
    _subscribers.add(handler);
    return () => _subscribers.delete(handler);
  }, []);

  // t est mémorisé (useCallback) avec [lang] comme seule dépendance : sa
  // référence ne change que si la langue change réellement. Sans ça, t était
  // recréée à CHAQUE render de CHAQUE composant utilisant useLang(), ce qui
  // cassait tout composant la mettant dans un tableau de dépendances
  // (useCallback/useEffect) — boucle infinie render → effet → fetch →
  // setState → render → nouvelle référence t → effet → ... (cause du
  // clignotement du panneau News Bibliothèque).
  const t = useCallback((key, vars = {}) => {
    const dict = TRANSLATIONS[lang] || TRANSLATIONS.fr;
    let str = dict[key] ?? TRANSLATIONS.fr[key] ?? key;
    for (const [k, v] of Object.entries(vars)) {
      str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v));
    }
    return str;
  }, [lang]);

  return { lang, t, setLang, supported: SUPPORTED_LANGS, meta: LANG_META };
}

